#!/usr/bin/env node
/**
 * Prepares live environment from conf/start.md
 * Reads backend URL and writes .env.production.local for next build
 * Usage: node scripts/prepare-live-env.mjs
 */

import { readFileSync, existsSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = process.cwd();
const startMd = join(rootDir, 'conf', 'start.md');

function extractUrl(content, section) {
  const regex = new RegExp(`${section}:\\s*(https?:\\/\\/[^\\s#\\n]+)`, 'i');
  const sameLine = content.match(regex);
  if (sameLine) return sameLine[1].replace(/\/+$/, '');
  const lines = content.split('\n');
  let inSection = false;
  for (const line of lines) {
    const trimmed = line.trim();
    if (new RegExp(`^${section}:\\s*$`, 'i').test(trimmed)) {
      inSection = true;
      continue;
    }
    if (inSection && trimmed && !trimmed.startsWith('#')) {
      if (/^https?:\/\//i.test(trimmed)) return trimmed.replace(/\/+$/, '');
      inSection = false;
    }
  }
  return null;
}

if (!existsSync(startMd)) {
  console.error('conf/start.md not found. Create it with backend: and URL on next line.');
  process.exit(1);
}
const content = readFileSync(startMd, 'utf8').replace(/\r\n/g, '\n').replace(/\r/g, '\n');

const backend = extractUrl(content, 'backend');
if (!backend) {
  console.error('backend: URL not found in conf/start.md. Add:');
  console.error('backend:');
  console.error('http://fs26-back.felixseeger.de');
  process.exit(1);
}

const frontend = extractUrl(content, 'frontend') || backend;

const envContent = `# Auto-generated by scripts/prepare-live-env.mjs - do not edit manually
# Live config from conf/start.md
WORDPRESS_API_URL=${backend}
NEXT_PUBLIC_WORDPRESS_API_URL=${backend}
NEXT_PUBLIC_SITE_URL=${frontend}
`;

const envPath = join(rootDir, '.env.production.local');
writeFileSync(envPath, envContent, 'utf8');
console.log('Live env prepared:', envPath);
console.log('WORDPRESS_API_URL=', backend);
console.log('NEXT_PUBLIC_SITE_URL=', frontend);
